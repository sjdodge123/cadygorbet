        <div class="col-md-8 blog-text">

        	<p></p>

            <p>The fall floral and decor at the Dollar Tree this year is amazing. I bought a bunch of flowers and put together 4 wreath designs. I think they turned out really great, and the best part is that they're extremely budget friendly. The first design I'm going to show you is an upcycle using a base that I already had, and the other three are 100% made from Dollar Tree items. I hope you enjoy!</p>

            <a href="wreaths.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreaths.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>
           
            <br/>

            <p>For the first DIY, I took a wire pumpkin base that was given to me and used some fall leaves from the Dollar Tree to upcycle it. </p>

            <a href="step16.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step16.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

             <div class="center">
                <p>Here are the supplies I used..</p>      
            </div>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="supplies.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <div>
             	<ul>
             		<li>Pumpkin Shaped Base - this was given to me, but I think it was originally from a decorative fall piece sold at Wal-Mart</li>
             		<li>Fall Leaves - Dollar Tree</li>
             		<li>Mod Podge - Dollar Tree</li>
             		<li>Foam Brush- Dollar Tree</li>
             		<li>Hot Glue Gun</li>
             		<li>Floral Wire</li>
             		<li>Wire Cutters</li>
             		<li>Scissors</li>
             		<li>Twine - Dollar Tree (Not Pictured)</li>
             	</ul>
            </div>

            <p>I decided to make this one monogrammed, so that added some extra supplies that aren't from Dollar Tree.</p>

            <div>
             	<ul>
             		<li>Wood Letter - Hobby Lobby</li>
             		<li>White Paint - Michaels (Martha Stewart Satin Paint in 'Wedding Cake')</li>
             		<li>Paint Brush</li>
             		<li>Hammer</li>
             		<li>Nails</li>
             	</ul>
            </div>

            <p>I'm pretty sure you can get most of this entire supply list from Dollar Tree (like the wire and tools, etc.), but I already owned a lot of that stuff.</p>

            <br/>

            <p><strong>Step 1 - Paint Your Letter</strong></p>

            <p>I painted my letter white so it would stand out from the orange leaves. I did two coats of acrylic paint.</p>

            <a href="step1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 2 - Prepare Your Leaves</strong></p>

            <p>I just pulled the leaves off of some Dollar Tree stems. It's very easy to do and doesn't require any tools.</p>

            <a href="step2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 3 - Hot Glue Your Leaves</strong></p>

            <p>I started adding leaves to the base using hot glue. I just kept adding them until eventually the whole base was covered. You want to make sure you have good coverage, so layer the leaves on top of each other and be sure not to leave any gaps. It's okay to let the leaves hang over the edges; we'll trim those later.</p>

            <a href="step4.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step4.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <a href="step5.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step5.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <div class="center">
                <p>This is how mine looked completely covered.</p>      
            </div>

            <a href="step6.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step6.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 4 - Mod Podge</strong></p>

            <p>I went over the entire piece with a generous layer of mod podge to ensure all the leaves were stuck down flat. This will also make the piece more durable.</p>

            <a href="step8.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step8.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <a href="step9.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step9.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 5 - Trim Edges</strong></p>

            <p>Once it dried, I used scissors to trim the edges to bring back the pumpkin shape.</p> 

            <a href="step10.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step10.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 6 - Attach Letter</strong></p>

            <p>I added some nails to the back of my letter to work as hooks to anchor the letter to the pumpkin. I wrapped some floral wire around the nails then poked a hole through the pumpkin where the letter would attach.</p>

            <a href="step11.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step11.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <a href="step12.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step12.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <p>I used scissors to help poke the holes because the mod podge had made the leaves sturdier.</p>


            <p>Then, I tightly wrapped the wire around the base to keep the letter in place.</p>

            <a href="step13.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step13.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>I had originally planned on being done at this point, but I wanted to add someting to the stem because I didn't like how it looked.</p>

            <a href="step14.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step14.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p><strong>Step 7 - Wrap Stem</strong></p>

            <p>I wrapped some twine around the stem of the pumpkin to finish it off, using hot glue to secure it. And it's done!</p>

            <a href="step15.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step15.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <div class="center">
                <p>I think it turned out really cute, and it cost me less than $10 to make.</p>      
            </div>

            <a href="step16.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="step16.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>
            <br/>

            <p>For the next three wreaths, I don't have a step by step tutorial. I just picked up some wreath supplies and a bunch of flowers I liked from Dollar Tree, and when I got home, I played around with flower designs until I came up with some that I liked. (Note: I ended up not using the twine or burlap trim for any of these wreaths.)</p>

            <a href="pt2.1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="pt2.1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>I was really excited to find these these metal words. I've never seen these at the Dollar Tree before, and I couldn't believe you get a pack of 3 for $1. They're pretty small, compared to the ones they sell at places like Hobby Lobby, but if you get your wire wreath forms from Dollar Tree, they fit perfectly with those.</p>

            <a href="pt2.2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="pt2.2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>So, I made three wreaths using a different metal word for each wreath. I attached everything using a combination of floral wire and hot glue.</p>

            <div class="center">
                <p>For the first one, I used 'welcome' with red, orange, and yellow mums.</p>      
            </div>

            <a href="wreath1.1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath1.1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>
           
            <a href="wreath1.2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath1.2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

        	<br/>

        	<div class="center">
                <p>For the second, I used 'harvest' with orange sunflowers.</p>      
            </div>

            <a href="wreath2.1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath2.1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>
           
            <a href="wreath2.2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath2.2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>On the third wreath, I cheated a little bit by using a gold spraypaint (not from the Dollar Tree) to spray 'thankful' and the wreath form. I already had the spraypaint, but it's just one of the small Krylon shortcuts. Using a coupon, it only costs about $2 from Michaels ($4 full price). I used the paint because I wanted to leave part of the wire exposed, and filled in some floral accents using leaves, berries, and sunflowers.</p>

            <a href="wreath3.1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath3.1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <a href="wreath3.2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="wreath3.2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>A good tip when working with faux flowers is to use a hair staightener to fix the petals. Just be sure to use a low heat setting because you don't want to burn or melt any of your flowers. I wouldn't reccommend using the same one you use for your hair either. A mini one would actually be perfect to keep with your craft supplies, and you can even use this same technique to straighten out ribbon. Here's a before and after of some of the flowers I did before I attached them to my wreath. It made such a difference!</p>

            <a href="tip.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="tip.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>This last part I want to share isn't really a DIY, but it's all from the Dollar Tree, so it's still relevant. Back in spring/summer, I bought these adorable galvanized planters from Dollar Tree. I've had them stored away, but I wanted to bring them out to display some more autumnal flowers on my back deck. I weighted down the planters with river rocks from Dollar Tree, and just stuck a few stems in each of them.</p>

            <a href="planters1.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="planters1.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <p>Unfortunately, these specific 'flowers & garden' planters were seasonal for spring/summer, so they're no longer avaiable in Dollar Tree. But they currently have some cute fall themed ones available. And you can always check next spring and see if Dollar Tree brings back the 'flowers & garden' planters.</p>

            <a href="planters2.jpg" data-toggle="lightbox" data-gallery="Fall_Dollar_Tree_DIYs" data-type="image"><img src="planters2.jpg" alt="Dollar Tree DIY, Fall DIY, DIY Fall Wreath, Fall Floral" class="img-fluid"></a>

            <br/>

            <p>I hope you enjoyed this post and got inspired to make your own budget friendly fall wreaths. Thanks for reading!</p>

           

       

