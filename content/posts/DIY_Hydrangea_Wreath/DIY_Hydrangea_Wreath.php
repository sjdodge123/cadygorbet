        <div class="col-md-8 blog-text">
            <p>I recently made a wreath for my front door using hydrangeas. I thought it turned out beautifully, so I wanted to share how I made it. It’s very easy to put together, and because the hydrangeas are so lush, it doesn’t take a lot of pieces to make the wreath look full. I was inspired by <a href="https://www.pinterest.com/pin/415034921893798644/">this wreath</a> that I saw on Pinterest (which links to an <a href="https://www.etsy.com/shop/HomeHearthGarden?ref=l2-shopheader-name">Etsy shop</a> that sells very beautiful handmade wreaths).
            </p>
            <br/>

            <p>So here’s what you’ll need to make this wreath..
            </p>
            
            <div class="center">
                <p>9 Hydrangea Flower Stems (3x of 3 different colors)</p>
                <p>18” Wire Wreath Frame</p>
                <p>Floral Wire</p>
                <p>Wire Cutters</p>
                <p>Floral Accents (optional)</p>
            </div>
            <a href="wreathsupplies.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="wreathsupplies.jpg" alt="DIY Hydrangea Wreath Supplies, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>
            <br/>

            <p><strong>Step 1</strong>
            </p>

            <p>Using your wire cutters, remove the head of the hydrangeas from the flower stems.
            </p>

            <a href="preparedflowers.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="preparedflowers.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>
            <br/>

            <p><strong>Step 2</strong>
            </p>

            <p>Position the flowers on your wreath frame to see how you would like them to be arranged. The wreath frame is divided into 9 sections, so adding one flower to each section gives you the perfect spacing. Attach them using your floral wire.
            </p>

            <a href="onefloweronwreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="onefloweronwreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>

            <p>I cut about a foot of wire for each flower. Begin by weaving the wire through the center of the flower and attaching the base of the hydrangea to the frame, wrapping the wire around the frame until it feels secure. Attach a few other stem pieces to the frame, just to stabilize it a bit more and to help it keep its shape.
            </p>

            <a href="flowerwiring1.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="flowerwiring1.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>

            <a href="flowerwiring2.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="flowerwiring2.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>

            <a href="flowerwiring3.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="flowerwiring3.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>

            <p>Repeat this step for all 9 of your flowers.
            </p>

            <a href="completedwreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="completedwreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>
            <br/>

            <p>Now, you could leave it as is with just the flowers, but I took some of the leaves off of the stems and added them for a bit of greenery.
            </p>
            <br/>

            <p><strong>Step 3</strong>
            </p>

            <p>Add details or accents to your wreath if you so desire. Use your wire cutters to trim and shape your accents if needed, and attach them to the wreath using floral wire. 
            </p>

            <a href="leafaccents.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="leafaccents.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>

            <a href="summerhydrangeawreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="summerhydrangeawreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>

            <p>Depending on your taste and what kind of wreath you’re making, you can add in whatever kind of accents you want. Things like berries and sprigs and even butterflies would be great details to add. And again, it doesn’t need a whole lot of extra things because the hydrangeas themselves fill out the wreath nicely.  
            </p>
            <br/>

            <p><strong>Step 4</strong>
            </p>

            <p>Hang your wreath! 
            </p>

            <a href="summerhangingwreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="summerhangingwreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>

            <p>Because I am hanging my wreath on our front door, I chose my hydrangea colors to match the flowers we keep on our porch for spring and summer. I love the way it looks all together!
            </p>
            <br/>

            <a href="frontporchwreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="frontporchwreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Summer Decor" class="img-fluid"></a>
            <br/>

            <p>I love this wreath because it doesn’t take a lot of different supplies or much effort to put together, but it looks so pretty and substantial. I like it so much that I also made one for fall time using red, orange, and yellow hydrangeas (accented with fall leaves and berries). 
            </p>

            <a href="fallhangingwreath.jpg" data-toggle="lightbox" data-gallery="DIY_Hydrangea_Wreath" data-type="image"><img src="fallhangingwreath.jpg" alt="DIY Hydrangea Wreath, DIY Wreath, DIY Home Decor, Fall Decor" class="img-fluid"></a>
            <br/>

            <p>I hope you enjoyed this post and got inspired to DIY your own wreaths!
            </p>

            



           

