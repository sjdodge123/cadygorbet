			<p>In this post, I'm going to share how to carve foam craft pumpkins. I first saw craft pumpkins while shopping at Michaels a few years ago. They had a book at their floral desk where you could pick out a design and have a pumpkin ordered and laser cut with your design. While I was there, I heard someone say that you could actually do it yourself using a hot knife and get the same effect. So I decided to get one and try it out. It turned out awesome, and I've been a little bit addicted to carving craft pumpkins ever since.</p>

			<br/>

			<p>**Disclaimer** This project is not something kids should be doing. Even with adult supervision, children should not be handling a hot knife. It can be dangerous for adults as well, so use your best judgment and take precaution.</p>

			<br/>

			<p>I have quite a few pumpkins that I've made over the past couple years, and I made a new one to add to my collection this year. While I was carving my new pumpkin, I decided I'd also make a post showing others how I make them. It's really simple, though it does take a lot of patience and a pretty steady hand. I just apply the same method I use to carve real pumpkins to the craft pumpkins, and it works perfectly. And the craft pumpkins will last year after year!</p>

			<br/>

			<p>Here's what you'll need..</p>
           
            <a href="pumpkins.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="pumpkins.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <div class="halloween">
             	<ul>
             		<li>A Foam Craft Pumpkin - Michaels has them in 3 sizes</li>
             	</ul>
             	<div class="tip">If you get your foam pumpkin from Michaels, don't pay full price for it. They have sales all the time, so I only purchase pumpkins when they're 50% off or more. If they happen to be full price, use a coupon. Michaels has their own coupons and also take competitor coupons (Hobby Lobby, Joanns, etc.) as well.</div>
            </div>

            <br/>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="supplies.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

             <div class="halloween">
             	<ul>
             		<li>A Hot Knife (I used my wood burner, which came with a blade attachment)</li>
             		<li>A Print Out of Your Design</li>
             		<li>An Embroidery Needle (a thimble may be useful for you, too)</li>
             		<li>A Pen</li>
             		<li>Scissors</li>
             		<li>Tape</li>
             	</ul>
            </div>

            <br/>

            <p>First, you'll want to choose the size of your pumpkin. For this tutorial, I chose to go with the medium 9" pumpkin. Then, choose your design. I chose to do a scene from It's the Great Pumpkin, Charlie Brown. You can do an image search of 'pumpkin stencils' and browse or you can search for a specific stencil. You'll want to keep in mind the shape and complexity of your design and which pumpkin size you have. For example, if you are working on a small pumpkin, you don't want a design that has a lot of little details because it will be difficult to cut out the shape. Likewise, if you are working on one of the large pumpkins, you'll want a design that is taller than it is wider to better fit the pumpkin's shape.</p>

            <br/>

            <a href="stencil1.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="stencil1.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>Print out your stencil, and attach it to your pumpkin with some tape. Because of the pumpkin's curved shape, it can be a little tricky to get the stencil smoothed down. It's easier to work with if you do a rough cutout of your design and get rid of excess paper, but just do the best you can.</p>

            <br/>

            <a href="stencil2.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="stencil2.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>Next, you're going to take your needle and poke holes along your stencil in order to transfer the shape to your pumpkin. The black part of the image is the part you'll be cutting out, so be sure to have your holes stay pretty precise on the line or inside the line on the black.</p>

      	
            <div class="tip">This part can wear on you fingers, so using a thimble can help.</div>
            
            <br/>

            <p>When you're done, it should look like this.</p>

            <br/>

            <a href="stencil3.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="stencil3.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <a href="outline.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="outline.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>I recommend going back and tracing your dots before cutting so that you can see you image better. But remember to trace your dots exactly or trace where you'll be cutting out so you don't have pen marks left on your pumpkin.</p>

            <br/>

            <p>It's time to carve your pumpkin. Heat up your hot knife, and carefully start cutting. In my experience, you have to cut slowly and take your time. It's easier to do larger chunks first and go back and clean up lines after. Also, curves can be tricky, so I like to cut around those in small sections rather than all at once to keep my lines precise.</p> 

            <br/>

            <a href="cutout1.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="cutout1.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>Here is my pumpkin after a rough cutout of the larger areas of my design. I find it easier to do this and then go back and cut out the smaller details.</p>

            <br/>

            <a href="cutout2.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="cutout2.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>Be sure to cut a hole in the bottom for your light. **Do not use a real flame!** A lot of stores sell LED pumpkin lights that flicker or even change color, so be sure to use something like that instead of a real candle.</p>

            <br/>

            <a href="finalpumpkin1.jpg" data-toggle="lightbox" data-gallery="Craft_Pumpkin_Carving" data-type="image"><img src="finalpumpkin1.jpg" alt="DIY Pumpkin, Craft Pumpkin Carving, Halloween, Pumpkin Carving, Jack-o-Lanterns, Halloween DIY" class="img-fluid"></a>

            <br/>

            <p>Here's what the final pumpkin looks like!</p>

