            <p>I've been seeing these vertical signs everywhere lately, and I think they're so cute and look great on porches or next to the front door. I've mostly seen 'welcome' signs, but with the holiday season coming up I've started seeing autumn and Christmas themed signs as well. I decided I'd like to try making my own sign(s), that way I can pick the size, color scheme, and word that I wanted. It wasn't difficult to do, but I don't necessarily feel like this is a cheaper alternative to buying one. Unfortunatly, I didn't keep a tab, but I'd estimate that the harvest sign cost me about $35 to make (some supplies I already had on hand). I've seen signs in stores for cheaper than that and some that are more expensive. But I love making my own stuff, so I don't feel bad for putting some extra money towards my hobby. So lets start making some autumnal vertical porch signs!</p>
          <br/>
            <a href="autumnalsigns.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="autumnalsigns.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>

            <p>Here are the supplies needed...</p>
		
		<div class="center">	
      		<p>A Wood Board</p>
      		<p>Wooden Letters</p>
			<p>An Electric Sander or Sandpaper</p>
			<p>Wood Stain and A Rag</p>
			<p>A Paint Brush or Foam Brush</p>
			<p>Paint</p>
      		<p>Wood Glue</p>
      		<p>A Tape Measure</p>
      		<p>A Pencil</p>
			<p>Decorative Accents - I ended up only using the big wooden leaves, one pumpkin, and smaller wooden leaves (not pictured)</p>
		</div>
          <br/>

			<a href="supplies1.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="supplies1.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>

       		<a href="supplies2.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="supplies2.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>

       		<a href="supplies3.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="supplies3.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>
          <br/>

			<p>Step 1</p>
			
			<p>To begin, you're going to want to pick your word and board size. I bought some wood letters at Michaels to spell out the words 'harvest' and 'fall'. Here's a <a href="http://www.michaels.com/8in-classic-wood-letter-by-artminds/M10492882.html?dwvar_M10492882_color=Unfinished%20-%20A#pmpt=qualifying&sz=24&start=27">link to the letters</a> that I bought, in case you want to use the same ones. I used my letters to determine what size board I needed to get. For 'harvest' I used a 1x12 - 6ft common board from Home Depot. For 'fall' I used some scrap wood from my parents' house that happened to be the perfect size. My 'harvest' sign is pretty tall at 6ft, so for a smaller sign, you can pick a shorter word or use smaller letters. </p>
          <br/>
          <br/>
			
			<p>Step 2</p>
			
			<p>Lightly sand your board to prep it for staining. Sand, and wipe away excess dust with a wet rag. Be sure to wear eye protection and a dust mask while sanding.</p>
		  <br/>
			<a href="sand.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="sand.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>
          <br/>
			
			<p>Step 3</p>
			
			<p>Stain your board. I used the Rust-oleum brand stain in Dark Walnut to stain both of my boards. Do as many coats as you'd like. I applied one coat to each board using a rag. Follow the directions on your stain's packaging for application instructions and dry time. </p>
		  <br/>
			<a href="stain1.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="stain1.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>

			<a href="stain.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="stain.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>
          <br/>
			
			<p>Step 4</p>
			
			<p>Time to paint your wooden letters. I did a light base coat of white first, then I decided to alternate red, yellow, and orange for 'harvest' but do all orange letters for 'fall'. I used multi-surface satin acrylic paint for this. </p>
		  <br/>
			<a href="paintletter.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="paintletter.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>

       		<a href="whiteletters.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="whiteletters.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>

       		<a href="paintedletters.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="paintedletters.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>
          <br/>

			<p>Step 5</p>
			
			<p>Now, you can decide whether or not to add any extra accents to your sign. I had a bunch of stuff like ribbon, rafia, and burlap to maybe make a bow or some trim, but in the end I chose to add only some wooden leaf cutouts and one of the wood pumpkins. The wooden cutouts used on the 'harvest' sign were from the dollar/impulse section of Target. They have an adhesive backing on them, so all I had to do was paint them then stick them onto the board. The larger leaf cutouts and pumpkin that I used on the 'fall' sign were both from Wal-mart in their fall seasonal section. The wooden leaves origninally had some felt raccoons on them, which I just removed before painting. And the pumpkin had a wire for hanging, which I also removed before adhearing. </p>
          <br/>
          <br/>
			
			<p>Step 6</p>
			
			<p>Time to lay all of your pieces out onto your board and decide how you would like them to be arranged. I used a measuring tape and pencil to help me space the letters out evenly. </p>
		  <br/>
			<a href="letterplacement.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="letterplacement.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
       		</a>
          <br/>
          <br/>
          <br/>

			<p>Step 7</p>
			
			<p>Now, it's time to attach your pieces to your board. I used wood glue to attach everything, then placed heavy books on top as it dried. </p>
			
			<p>And we're done!</p>
		  <br/>
      <a href="harvestsign.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="harvestsign.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
          </a>

          <a href="fallsign.jpg" data-toggle="lightbox" data-gallery="DIY_Autumnal_Porch_Sign">
                <img src="fallsign.jpg" alt="fall decor, diy fall decor, autumn decor, porch sign, diy wood sign" class="img-fluid">
          </a>
          <br/>
          <br/>
			
			<p>I think they both turned out great. There are a few things I would have tweaked here and there, but overall I'm happy with the final product. This same concept can be used for other holiday or everyday signs. You could use stencils or freehand your letters/designs instead of using wood cutouts. You could also do a light sanding to the finished product to give it a rustic appearance. When making your own sign, there are so many options you can choose to reflect your personal style.</p>

			<p>**I want to mention that I made these signs knowing I would be placing them either inside the house or outside on our covered porch, so I didn't take any preventative measures for weathering or outside wear and tear. If you want to use these signs outside in an open space, I would recommend using treated wood and researching a weatherproof sealer or varnish so that you can preserve your work.**</p>
