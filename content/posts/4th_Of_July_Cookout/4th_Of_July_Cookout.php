            <p>We hosted a 4th of July cookout at our home this year. I had fun planning, decorating, and even doing some DIYs, so I wanted to share some photos from the event. </p>

            <p>First up, the fireplace. I absolutley loved everything about it and how it all looked together. </p>
            <a href="4th_of_july_fireplace.jpg" data-toggle="lightbox" data-gallery="4th_of_July_Cookout" data-type="image"><img src="4th_of_july_fireplace.jpg" alt="fireplace decorated for 4th of July" class="img-fluid"></a>
            <br/>
            <p>It’s simple because I didn’t want to go overboard or have things too cluttered. The bunting consisted of some red, white, and blue string lights from Target ($3) and a burlap banner that I found at Cost Plus World Market. The burlap fits perfectly with the rustic style that I love, and also it keeps the space from looking overwhelmingly red, white, and blue because it breaks up the color a bit. I actually hung the bunting using command hooks that I have ‘permanently’ underneath our mantle. Command hooks are great because you can remove them without causing damage, but I keep these ones up so I can change out bunting or banners for different occasions. </p>
           
            <p>The last pieces for the fireplace are my DIY rockets and dynamite. 
            <a href="diy_rockets.jpg" data-toggle="lightbox" data-gallery="4th_of_July_Cookout" data-type="image"><img src="diy_rockets.jpg" alt="diy 4th of July rocket and dynamite decor" class="img-fluid"></a>
            <p>These were honestly sort of a pain to make, but I’m so pleased with how they turned out that I’m glad I took the time and effort to make them. I’ll have them basically forever now, so I can use them again and again. I saw some inspiration pictures for <a href="https://www.pinterest.com/pin/415034921901281713/">DIY rocket décor</a> on Pinterest and wanted to try it out. Instead of using paper towel or toilet paper rolls to make these, I used some shipping tubes I had laying around which worked great and allowed me to make them on a larger scale. </p>

            <p>I had two shipping tubes to work with, so I cut each of them in half (carefully, using a large serrated kitchen knife) to give me four bases. I then sanded down the raw edges. Because one of the tubes was a little smaller and fatter, I decided the shape would work great for making sticks of dynamite, so I ended up with two rockets and two dynamites. I used satin acrylic paint and painters tape to make my designs. I had to do several coats and touch ups, which was the frustrating and time consuming part. The tops of the rockets are made out of poster board which I cut into circles and made into cone shapes, then attached with hot glue. The fuses on the dynamite are made from a rope-like string that I found in my craft supply collection. I ended up threading some floral wire through the string to allow it to stand up after it had been glued on. I’m very pleased with the result, and I loved these pieces as part of our party décor. </p>
            <br/>
            <p>We had two tables for food set up. I had themed table clothes, napkins, and plates. I had a few party décor pieces around the food area like a banner, some balloons, and I put some flags and picks in mason jars to set on the tables as well. I like to have something on the table that’s not just food to act as a centerpiece and dress up the space. </p>
           
