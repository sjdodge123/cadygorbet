            <br/>
            <p>Added some balloons around the banner in the dining room and in the living room, some garland around the door frames, and the party decorations were done.</p>
            <a href="4th_of_july_party.jpg" data-toggle="lightbox" data-gallery="4th_of_July_Cookout" data-type="image"><img src="4th_of_july_party.jpg" alt="diningroom set up for 4th of July party" class="img-fluid"></a>

