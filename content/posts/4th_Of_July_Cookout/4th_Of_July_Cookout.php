            <br/>
            <p>We have a chalkboard on the wall in the dining room that I’ll change for certain events and holidays, so I wrote something 4th related on it for our cookout. I copied <a href="https://www.pinterest.com/pin/AaZ59ILdEf1SdsRRX0re_atIm7t632nSoqRpGP2r3rDZDRPcOun1w3Y/">this one</a> straight from Pinterest.</p>
