            <p></p>

            <p>I recently DIYed a rustic floating shelf for our master bedroom. I'm so happy with the result; it looks exactly how I envisioned. It wasn't costly & was very easy to make. Here's how you can do it...</p>
            

            <p>Supplies Used</p>

            <div>
                <ul>
                    <li>Wood Board - my board was a 1x10 8ft common board</li>
                    <li>L Brackets - <a href="https://www.amazon.com/dp/B07FQRK6GS?ref=ppx_pop_mob_ap_share&fbclid=IwAR1BV0b818MNCikuzJJ5Asntc_b29Pg1NIWJ0fjajU82Fr6uCTS734pFV0o">these are the ones I used</a></li> 
                    <li>Saw - depending on if you need your board a little shorter (you could also get it cut at the hardware store)</li>
                    <li>Electric Sander or Sandpaper</li>
                    <li>Wood Stain & Rag</li>
                    <li>Drill</li>
                    <li>Measuring Tape</li>
                    <li>Wall Anchors</li>
                </ul>
            </div>

        

            <p>The first step is to determine how long you want your shelf to be. My board was 8 feet long, and I cut off about 6 inches.</p>

            <a href="step1.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step1.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>Next, you want to sand your board to smooth out the texture & prep it for staining.</p>

            <a href="step2.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step2.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>After sanding, wipe down the board with a damp cloth to remove any debris.</p>

            <a href="step3.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step3.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>These (pictured below) are the two stains that I used. I did one coat of the dark walnut color first. Then, I added a light coat of the weathered gray. I used my scrap piece of wood to test stains before staining the board, which is very important. I would not recommend staining your board without testing, especially if you're going to layer stains like I did. I used a cloth to apply my stain, but, as you test, you can try out different techniques and methods for application.</p>

            <a href="step4.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step4.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>When I was testing stains, I found the dark walnut to be too warm on its own, so I added a light layer of gray to bring some coolness to it. This gray stain applies very opaque, so I used very little at a time and really made sure to buff in the color as to not cover the walnut completely.</p>

            <div class="center">
                <p>This was my board after one coat of the dark walnut.</p>
            </div>


            <a href="step5.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step5.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <div class="center">
                <p>This is when I began to add on the gray stain.</p>
            </div>

            <a href="step6.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step6.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>Here is a close up of the finished stained look. Be sure to stain the sides & bottom of your piece, too.</p>

            <a href="step7.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step7.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>Next, you'll want to add your L brackets.</p>

            <a href="step8.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step8.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>Now, my idea on how to make this shelf a 'floating' shelf was to use the L brackets upside down. Instead of having them come out of the bottom of the shelf, you'll want them sticking up out of the top. My plan was to hide the brackets using decor pieces once the shelf was up on the wall. Because you wont see the brackets on the bottom, and the top parts will be hidden, it'll give the appearance of a floating shelf.</p>

            <p>I decided to use 4 brackets for my shelf. You may want to use more or less, depending on how long your shelf is and how heavy of items you're planning to put on it. You may also want to get longer brackets if your shelf is wider. Use a measuring tape to evenly space out the brackets.</p>

            <a href="step9.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step9.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>Predrill holes, and add your brackets with the longer side attched to the bottom of the shelf for greater support. Remember, you want your brackets to be upside down, so be sure you attach them in the way that ensures the part attaching to the wall comes out of the top.</p>

            <a href="step10.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step10.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>


            <a href="step11.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="step11.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>With the shelf rightside up, your brackets should all look like this.</p>

            <a href="bracket.jpg" data-toggle="lightbox" data-gallery="DIY_Rustic_Floating_Shelf" data-type="image"><img src="bracket.jpg" alt="DIY Shelf, DIY Floating Shelf, DIY Rustic Floating Shelf, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>

            <p>This completes your shelf! All that's left is to hang it up.</p> 

            <p>**NOTE** The brackets I purchased came with wall hanging hardware, but I purchased some heavier duty wall anchors to use instead. Just make sure whatever you use will safely and securely mount your shelf to the wall.</p>

