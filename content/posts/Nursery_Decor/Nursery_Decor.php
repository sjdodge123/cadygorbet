            <p>In this post, I'm sharing some pictures of our baby's nursery. Over the summer, I remade our guest bedroom into a nursery for our first baby, due in fall. I repainted, got new curtains and all new furniture (except for the dresser), and cleared out the room's closet, which was home to all of my craft supplies. It was a big undertaking, but I tackled it bit by bit, and now it's finally ready. I'm sure once the baby is here and we're using the room I may add things or move stuff around to be more practical, but for now I'm happy with the set up. There's really nothing that needs to be explained, so this will basically just be me sharing some inspiration pictures. However, I will list (and try to provide a link to) where we purchased items incase you're looking to buy the same things. Enjoy!</p>

           
            <p>Just to give you some context for the layout of the room, one wall of the nursery is mostly a large window, and another wall is taken up by the bi-fold closet doors, so there's only really two free walls in the room. Because of this, all of the furniture is kind of just lined up against the two empty walls. It's not aesthetically ideal, but it works for what it is.</p>

