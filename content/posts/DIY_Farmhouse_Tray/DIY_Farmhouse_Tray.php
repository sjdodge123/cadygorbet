                    <li><a href="https://www.michaels.com/artminds-wood-candle-holder-6.75in/10154816.html">6.75 Wood Candle Holder</a></li>
                    <li>Wood Stain, Gloves, & Rag</li>
                    <li>Super Glue or Wood Glue</li>
                    <li>Needle Nose Pliers</li>
                    <li>Saw & Sandpaper/Electric Sander (not pictured)</li>
                </ul>
            </div>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="DIY_Farmhouse_Tray" data-type="image"><img src="supplies.jpg" alt="DIY Tray, DIY Farmhouse Tray, Farmhouse Decor, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>The first step I took was creating an even surface on the candle holder so the wood that creates the 'tray' would sit level. To do this, I took my pliers and bent and broke off the metal piece that was sticking out of the top.</p>

            <a href="candleholder.jpg" data-toggle="lightbox" data-gallery="DIY_Farmhouse_Tray" data-type="image"><img src="candleholder.jpg" alt="DIY Tray, DIY Farmhouse Tray, Farmhouse Decor, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>Next, I decided to detach the top piece of wood from the pedestal, just as a personal preference, to make it a little shorter and less bulky. I thought I could just pry it off using the pliers, but it was really secured together, so I ended up using a circular saw to cut it off. This left the top of the piece uneven, so I used my sander to smoothe it down.</p>

            <a href="pedestal.jpg" data-toggle="lightbox" data-gallery="DIY_Farmhouse_Tray" data-type="image"><img src="pedestal.jpg" alt="DIY Tray, DIY Farmhouse Tray, Farmhouse Decor, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>Next, I stained all of the pieces. You could use any stain or even use paint, depending on what sort of look and finish you like. I chose a gel stain in the color 'Dark Walnut'.</p>

            <a href="stain.jpg" data-toggle="lightbox" data-gallery="DIY_Farmhouse_Tray" data-type="image"><img src="stain.jpg" alt="DIY Tray, DIY Farmhouse Tray, Farmhouse Decor, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>When the stain dried, I stacked all the pieces together to make sure it was even and that I was happy with the look. This is basically what the tray looks like finished. I just glued everything together, and that completed my farmhouse tray.</p>

            <a href="tray.jpg" data-toggle="lightbox" data-gallery="DIY_Farmhouse_Tray" data-type="image"><img src="tray.jpg" alt="DIY Tray, DIY Farmhouse Tray, Farmhouse Decor, Rustic Home Decor, DIY Home Decor" class="img-fluid"></a>

