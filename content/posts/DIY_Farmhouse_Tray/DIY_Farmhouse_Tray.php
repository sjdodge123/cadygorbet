            <p>In this post, I'm going to show how I made a farmhouse inspired tray. These are all over Pinterest and Instagram, and they're a great piece to showcase everyday decor or seasonal decor. Here's how I made it..</p>
            


            <p>Supplies Used</p>

            <div>
                <ul>
                    <li><a href="https://www.michaels.com/artminds-round-clock-11in/10217380.html">11" Round Wood Clock Base</a></li>
                    <li><a href="https://www.michaels.com/pine-circle-plaque-by-artminds/10067602.html">8" Pine Circle Plaque</a></li> 
