        <div class="col-md-8 blog-text">

            <p>Over the years, I’ve accumulated a lot of different crafting supplies. Some stuff I’ve bought on a whim, some is left over from other projects. Sometimes, I just don’t like throwing stuff away if I feel I can do something or make something with it. From fabric to paint to wood to jewelry, I have a lot of different crafting items. The space in which I keep all of my materials had gotten pretty messy, so I recently cleaned and reorganized it. So for this post I wanted to share with you how I store and organize all of my supplies. 
            </p>
            <br/>

			<p>I keep all of my crafting supplies in our spare bedroom closet. When I was first setting up the space, I knew I wanted some sort of shelving or bookcase type thing to keep items more streamlined and assessable. Keeping things easy to locate and reach is important because I hate not knowing where things are or having to rummage to find certain items. I ended up purchasing a few different storage cube pieces from Michaels craft store, and I think they work great in the space. 
 			</p>
 			<br/>

 			<p>I have a couple different pieces that I want to talk about. The first one is the 2x3 11” cube.
 			</p>

            <a href="sixcubeunit.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="sixcubeunit.jpg" alt="6 cube storage unit for organizing craft supplies" class="img-fluid"></a>
            <br/>

            <p>I was lucky enough to find this a few months back on clearance at Michaels. Unfortunately, since it was on clearance that specific piece is no longer available, but cube units are very popular right now and I’ve seen them everywhere (Target, Wal-mart).  Now, I will say that it’s not the best quality, but for the price I figured it was good enough for sitting in the closet. I do love the overall unit though. I purchased some fabric inserts from Target when they were on sale for $5, and added them to the cubes. They help with storage and overall just make the unit look nicer. I currently have them filled with glass jars that I save from old candles. I just can’t bring myself to throw perfectly good glass containers away, but they’re a little difficult to store. With the bins, it hides all the jars and keeps them in a secure place where they won’t get knocked over or easily break. 
            </p>
            <br/>

            <p>The next pieces I got are also from Michaels, and they should all still be available (I always wait for a good coupon or 50-60% off sale). 
            </p>

            <a href="opencubeunits.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="opencubeunits.jpg" alt="storage cubes for craft supply organization" class="img-fluid"></a>
            <br/>

            <p>They are from the <a href="https://www.michaels.com/search?q=storage%20cubes">Recollections scrapbook storage</a> section and are sold individually, so you can create your own unit. These pieces are the 14” open cubes and have inserts (sold separately) with dividers and drawers (right). Now, for the two top left sections, I only bought the insert pieces and not the open cube they fit into. I was trying to save money and didn’t think the actual cube served any real purpose if I was just going to buy the inserts anyway. I will say that as a result, the drawers are not completely stable, and those pieces are now slightly smaller than the rest of the cubes. I don’t really mind this, but I may eventually get two more open cubes to ‘finish’ it. 
			</p>
			<br/>

			<p>In the two drawers, I keep my painting supplies. The top drawer holds all of my acrylic paints, foam brushes and rollers, and some extra plastic paint jars. Whenever I get a new acrylic paint, I always dot the top of the container with the color inside, that way when I store it I can easily see which color is which. 
			</p>

			<a href="paintdrawer.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="paintdrawer.jpg" alt="drawer organization for acrylic paint" class="img-fluid"></a>
            <br/>

            <p>The second drawer has other specialty paints that I don’t use as often. I have puffy paints, gold gilding, a watercolor pallet, and some oil paints. I also keep some sponge wedges and my glass etching cream in there. 
			</p>
			<br/>

			<p>I really love the bottom left corner of this section. This particular cube perfectly fits these 12x12” boxes that I use to house scrapbook paper, card stock, paper pads, and other paper items. 
			</p>

			<a href="paperstorage.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="paperstorage.jpg" alt="scrapbook paper storage boxes and storage cube" class="img-fluid"></a>
            <br/>

            <p>Open stock paper and paper pads can be difficult to store because you don’t want to stack a lot of them together, which makes it harder to find and access particular things. I like how this cube breaks up the storage with shelves and gives me four different sections to divide my paper. One of these 12x12” boxes has all of my paper and embellishments for a specific scrapbook I will be working on, so when it comes time to start that project, I’ll have all of my supplies neatly together and ready to go. 
			</p>
			<br/>

			<p>You’ll notice that I also have a lot of decorative storage boxes in the closet as well. Some of them fit in the cubes, or on top of the units, and some I have stored on the top shelf in the closet. 
			</p>

			<a href="topshelf.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="topshelf.jpg" alt="decorative storage boxes for organizing craft supplies" class="img-fluid"></a>
            <br/>

            <p>I absolutely love these types of boxes, and before we moved into our current house and acquired the closet space, those were all I had to keep my craft supplies in. I purchased the majority of them from Michaels, but a few are from Marshalls. You can also find some at Ross, TJ Maxx, and Home Goods. Each box is organized to hold a specific category of my supplies.
            </p>

            <a href="toolandadhesivebox.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="toolandadhesivebox.jpg" alt="decorative storage box holding craft tools and adhesives" class="img-fluid"></a>
            <br/>

            <a href="ribbonbox.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="ribbonbox.jpg" alt="decorative storage box holding ribbon and twine" class="img-fluid"></a>
            <br/>

            <p>Some examples of box categories are...
            </p>

            <p>Tools and Adhesives – glue gun and sticks, magnets, super glue, wire cutters, pliers, command hooks
            </p>

            <p>Ribbon – spools of ribbon, loose ribbon, and twine
            </p>

            <p>Wood Supplies – wood burning tools and accessories, small wooden shapes 
            </p>

            <p>Fabric – scrap fabrics and felt 
            </p>

			<p>There’s no other organization other than similar supplies all being kept in the same box, but it works well enough for me, and I like how the boxes add some decoration to the space as well as functionality.  
            </p>
            <br/>

            <p> On the floor to the left of the cubes, I just have my sewing machine and sewing supply box, a box of more (odd shaped) candle jars, and my paper cutter. 
            </p>

            <a href="sewingmachine.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="sewingmachine.jpg" alt="sewing machine and sewing supply box" class="img-fluid"></a>
            <br/>

            <p> But on the right side, I have something that I’m pretty excited about. Wrapping paper can be difficult to store neatly and keep organized, but I was seeing a lot of people on Pinterest using the <a href="http://www.ikea.com/us/en/catalog/products/80010222/">plastic bag holders from Ikea</a> to store their paper. So, I picked up a few of them and stuck them to the inside of the closet wall. 
            </p>

            <a href="wrappingpaperstorage.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="wrappingpaperstorage.jpg" alt="Ikea bag holder as wrapping paper storage" class="img-fluid"></a>
            <br/>

            <p> One of them holds wrapping paper, and the other has rolls of contact paper and some other cardboard tubes in it. The bag holders are working out great, and I may get another for the other side of the closet to hold flower stems.
            </p>

            <p> To finish off the gift wrap section, I used command hooks to hang some gift bags on the wall. Side note, I get a lot of my gift bags at the Dollar Tree. They’re $1 or sometimes 2 for $1, and they carry some really cute bags. 
            </p>

            <a href="giftwrapstorage.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="giftwrapstorage.jpg" alt="Ikea bag holder as wrapping paper storage, command hooks to hold gift bags" class="img-fluid"></a>
            <br/>
            <br/>

            <p> On the left side floor, I just have some bigger items like wreath forms and larger wood pieces. I keep my markers, colored pencils, and paint brushes out on display in some jars on the cubes. I also keep the boxes I go to more often in spots that are easier to reach, and the ones I don’t use as much are stored on the top shelf. 
            </p>
            <br/>

            <p> This is how the closet looked earlier in the week before I reorganized it.
            </p>

            <a href="closetbefore.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="closetbefore.jpg" alt="craft supply closet before organization" class="img-fluid"></a>
            <br/>

            <p> And this is how it looks now! 
            </p>

            <a href="closetafter.jpg" data-toggle="lightbox" data-gallery="Craft_Storage_and_Organization" data-type="image"><img src="closetafter.jpg" alt="craft supply closet organization, cube unit storage" class="img-fluid"></a>
            <br/>
            <br/>

            <p>I had a lot to go through, but I’m so happy I got all of my crafting supplies organized and looking good. It’s so nice to have the space to store everything, and I’m lucky to have gotten some great storage items for a decent price. I hope you enjoyed reading this post and hopefully took away some inspiration and/or tips for organizing!  
            </p>


