            <p>It's Christmastime! I put our Christmas tree up the weekend after Thanksgiving and decided to take some photos of the process. I want to share a pictorial/step-by-step of how I decorated our tree as well as share some photos of the new ornaments I picked up this year.</p>

            <br/>

            <p>Okay, let's set up the Christmas tree..</p>


            <p>The first thing I do when setting up a Christmas tree is fluff out the branches. Our current tree is my parents' old tree that they had for many years, so it's not as full as a newer tree would be. With time, bristles fall off and branches don't hold their shape as well, but if you really spend some time shaping and fluffing each branch, you'll see a huge difference in your tree's appearance. I spent a pretty significant amount of time with this, but my mom couldn't believe it was the same tree after she saw it, so I think it's definitely worth the time and effort..</p>

            <p>After fluffing, I string on the lights and add the star. I went with all white lights for our tree this year, but I love the look of colored as well.</p>

            <a href="tree1.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree1.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

            <br/>

            <p>I really like the more natural look that a lot of artificial trees have now. I like how they layer different types of branches and textures and how some even have pinecones. Since our tree doesn't have that look already, I decided to add some picks for texture. The ones I have were purchased from Michaels last year (2016).</p>

             <a href="accentpick.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="accentpick.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>These ones have red berries and pinecones and are a different texture than the branches of my tree. I have six of them that I randomly place throughout the tree. I don't really think I have enough to make a big impact, but I like how they add some color. I also love the pinecones because I have sort of a woodsy theme to my tree decor and the pinecones work great with that.</p>

             <a href="tree2.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree2.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>Next, I add the bulbs. My color scheme for bulb ornaments is pretty traditional. I stick with red, green, and gold, but I have them in varying sizes.</p>

             <a href="ornament1.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament1.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <a href="ornament2.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament2.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <a href="ornament4.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament4.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <a href="ornament3.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament3.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>I start with my largest bulbs, and put those towards the inside of the tree. I don't have a lot of these, maybe 6, but by placing them more towards the inside of the tree, I'm helping to fill in gaps which make the tree look fuller. Then I add the medium and small bulbs. By working with different sized bulbs and strategically placing them from the inside working towards the outer part of the tree, you can give the tree more dimention and depth.</p>

             <a href="tree3.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree3.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>The last things I add to the tree are my themed ornaments. I don't have a very strict theme, but I do tend to get ornaments that are woodsy or a little rustic. I have a lot that have pinecones, burlap, and tartan elements, but I'll get any that I just find cute.</p>

             <a href="ornament5.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament5.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <a href="ornament6.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament6.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <a href="ornament7.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="ornament7.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>Here's what the tree looks like with all the ornaments. Depending on the positioning of your tree, you might need to put bulbs around the entire tree. I put some on the backside of ours, but not nearly as much as the front. I also keep all of my individual themed ornaments towards the front so they can be seen.</p>

             <a href="tree4.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree4.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>Finally, add a tree skirt, and we're done!</p>

             <a href="tree5.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree5.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>Here's the tree with some presents and our stockings for the full effect.</p>

             <a href="tree6.jpg" data-toggle="lightbox" data-gallery="Christmas_Tree_and_Ornaments" data-type="image"><img src="tree6.jpg" alt="Christmas, Christmas Tree, Christmas Decorations, Christmas Ornaments, Christmas Decorating" class="img-fluid"></a>

             <br/>

             <p>I think it turned out pretty great, and I just love having the tree on at night when we sit in the living room. I hope this post was interesting and that you learned something and/or found inspiration for your own tree.</p>

