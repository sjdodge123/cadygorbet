            <p>A few notes about my supplies...the boxes I chose are 12"x12", but depending on your preference any box size will work as long as they are square. The craft sticks I used are also 12" long, so I know they fit my boxes. If you end up doing larger boxes (like 14"x14") you'll need longer craft sticks to fit them. I also determined for the 12"x12" boxes, 8" letters were the perfect size. If your boxes are larger or smaller, you'll have to adjust your letter size to fit.</p>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="supplies.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>The first thing you need to do is assemble the boxes. I needed 8 boxes for my design, so I only assembled the amount I needed. (The extra boxes will be used to store some stuff in our attic as my husband and I clear space in our house for the arrival of the baby). Instead of using packing tape to keep the boxes together, I used hot glue. This way I could glue the boxes together on the inside and wouldn't have any tape showing on the outside. </p>

            <p>Next, I painted the boxes white. Amazon actually sells these boxes in white, but they cost more than the plain cardboard. I knew I had a lot of white paint at home, so I didn't mind paying less for the cardboard color and painting them myself. So, I used some extra white trim paint and a roller I had on hand to paint all 8 boxes. Depending on the look and design you want, you may paint your boxes a different color, or even leave them brown.</p>

            <a href="paintingboxes.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="paintingboxes.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

        
            <p>It took a few coats, but this is what the boxes looked like after they were painted.</p>

            <a href="whiteboxes.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="whiteboxes.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>Next, you'll want to get your paint sticks and size them up to the boxes. The boxes are 12 inches, and the craft sticks I used are 12 inches, so for them to fit perfectly on the box without overlapping each other I had to trim them down a bit.</p>

            <a href="border.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="border.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>


            <p>I took rough measurements and used a miter box and hand saw to cut the craft sticks to size. </p>

            <a href="measuring.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="measuring.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <a href="cutting.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="cutting.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>Once all your pieces are cut, they should fit on the box like this.</p>

            
            <a href="border2.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="border2.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>


            <p>Now it's time to paint your letters and craft sticks. I purchased the letters used to spell out BABY BOY. I primed mine with the same white paint I used for the boxes, then I painted all the pieces blue.</p>

            <p>This is where you can get really creative with your blocks. I did a traditional blue because I'm having a boy, and my baby shower decor will be predominately blue. For a gender reveal, alternating pink and blue would be really cute. It would also be super cute to do each letter a different color, like the look of actual baby blocks. I think having each one a different color would be adorable if you were spelling out your baby's name. Something like that would work great as decor for a 1st birthday party or baby photoshoot.</p>

            <a href="priming.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="priming.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <a href="paintingletter.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="paintingletter.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            
            <p>These are all of my pieces after they were painted. Now, all that's left to do is attach them to the boxes.</p>


            <a href="paintedpieces.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="paintedpieces.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>


            <p>I used hot glue to attach the letters and craft sticks.</p>

            <a href="finishedblock.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="finishedblock.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>Additionally, I cut a teddy bear silhouette out of paper, painted it blue, and attached it to one of the blocks.</p>

            <a href="bearblock.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="bearblock.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>I added the teddy bear because I wanted each of the columns to be an even 4 blocks each. I picked a teddy bear specifically because my shower is bear themed.</p>


            <a href="finishedblocks.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="finishedblocks.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>


            <p>Here's the end result styled for my shower.</p>

            <a href="babyblocks.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="babyblocks.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>


            <p>I also took some baby bump pictures using the blocks as photo props.</p>

            <a href="maternityphoto1.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="maternityphoto1.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <br/>

            <a href="maternityphoto2.jpg" data-toggle="lightbox" data-gallery="DIY_Baby_Blocks" data-type="image"><img src="maternityphoto2.jpg" alt="DIY Baby Blocks, DIY Baby Shower, Baby Shower Decor, Giant Baby Blocks, DIY Baby Shower Decor, Maternity Photo Props" class="img-fluid"></a>

            <p>I think they came out super cute, and they weren't difficult to make. I love the dimensional look they have by using the craft sticks and wood letters. It makes the blocks look more realistic as opposed to if the letters/border were just painted on. A bonus, they're light weight, so stacking and moving them is really easy.</p>

            <p>Thanks for reading! I hope you enjoyed, learned something, and got inspired to DIY your own giant baby blocks.</p>



