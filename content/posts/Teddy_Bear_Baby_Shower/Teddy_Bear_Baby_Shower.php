            <p>In this post, I'm sharing some pictures from my baby shower. After seeing pictures on Instagram of Malika Haqq's baby shower, I immediately knew I wanted a teddy bear themed shower. Her shower was gorgeous! Hers was all warm, neutral colors, whereas I went with traditional blue (for a boy) with neutral brown accents. But I definitely took all my inspiration from pictures of her shower and pictures I saw on Pinterest.</p>

            <p>Because of Covid, my shower was small and had a pretty short time limit, so I knew I didn't want to sink a ton of money into it. I made a lot of the decorations myself, and I'm really happy with how it all looked. This post wont have any tutorials for the stuff I made because it was all pretty simple, but I will include a rough explanation of the materials I used for each little project.</p>

            <p>I'll start off by sharing my DIY invitations. As I said, I had a small guest list, so I didn't need to send out that many invites. I made these myself in a word program using pictures I found on Google Images. Along with the invite, I included a little card explaining the protective measures we would be taking because of Covid. Again, I made the card in my word program using pictures from Google. I sized the invites up to envelopes I already had and printed them out at home.</p>

            <a href="invitations.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="invitations.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <p>Here's an overview of the tables I set up. We had some finger foods set out on one table and desserts on the other. I layered blue and white table cloths and added blue pom-pom garlands, all of which are from Dollar Tree.</p>

            <a href="overview1.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="overview1.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <a href="bears.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="bears.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <a href="overview2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="overview2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

          
            <a href="overview3.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="overview3.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <p>I made the two garlands that hung over the main table. Looking back, I wish I would have moved the clock and strung the garlands over the dessert table. I think you would have been able to see them better there, and it would have made for a cuter display.</p> 

            <a href="garlands.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="garlands.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <p>To make the 'Boy Oh Boy' garland, I cut pennant shapes out of some burlap material I had on hand. I stenciled on the letters with chalk paint, then hot glued all the pieces to some twine.</p> 

            <a href="garland1.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="garland1.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <p>For the teddy bear garland, I printed out a bear and heart outline from Google Images, then used them as a template to cut the shapes out of blue and brown cardstock. I taped those on to twine.</p> 

            
            <a href="garland2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="garland2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <p>Like my invitations, I made these 'Ready to Pop' popcorn labels in my word program and printed them at home. I cut them out and taped them to brown cardstock, which I cut into scalloped circle shapes. I don't have a paper punch, so I used a stencil and scissors to cut the cardstock. It took a while to cut a bunch out, but I'm at home with a lot of free time, so I didn't mind.</p>


            <a href="popcorn.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="popcorn.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <a href="popcorn2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="popcorn2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            
            <p>Using the same blue and brown cardstock I used for my garland, I printed smaller versions of the bear and heart templates (also the same ones used for the garland) and used them to make these cupcake toppers. I added details to them using a lighter tan cardstock and drew on a bear face with a pen. These were very tedious and time consuming to make, but again, because of Covid, I had the extra time on my hands.</p>


            <a href="cupcakes1.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="cupcakes1.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <a href="cupcakes2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="cupcakes2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <p>I have two more 'DIY desserts' to share. First are just these bags of teddy grahams. Very simple, but they match with the bear theme. I tied them using some ribbon I had on hand.</p>

            <a href="teddygrahams.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="teddygrahams.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <p>Last are these cute little baby cookies I saw on Pinterest. I made them the night before the shower using Nutter Butters, blue Candy Melts, and brown icing.</p>


            <a href="nutterbutter1.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="nutterbutter1.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <a href="nutterbutter2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="nutterbutter2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>


            <p>Here are a few more pictures of the dessert table. I purchased the plates and napkins from Party City.</p>

            <a href="plate1.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="plate1.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <a href="plate2.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="plate2.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <a href="plate3.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="plate3.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <p>The last decorations were on the fireplace. This is where I sat to open gifts, so on either side of me I had my giant DIY baby blocks. In my <a href="http://cadygorbet.com/content/posts/DIY_Baby_Blocks/DIY_Baby_Blocks.php">previous blog post</a>, I have a step by step tutorial on how I made them. I also used some teddy bear onesies to make bunting for the mantle.</p>

            <a href="babyblocks.jpg" data-toggle="lightbox" data-gallery="Teddy_Bear_Baby_Shower" data-type="image"><img src="babyblocks.jpg" alt="Teddy Bear Baby Shower, DIY Baby Shower, Baby Shower Decor, Baby Boy Baby Shower, DIY Baby Shower Decor, Baby Shower Inspo" class="img-fluid"></a>

            <p>I'm really happy with how everything looked, and I love that I got to make a lot of it myself. Looking back, of course there are things I would tweak, move around, or add, but I think it turned out so nice, and I'm happy I got to celebrate our baby with the people closest to me.</p>

            <p>This was just a quick post to share some baby shower inspiration pictures, but I hope you enjoyed. Thanks for checking it out!</p>

           


