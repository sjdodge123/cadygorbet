       		 <p>Today, I'm going to show you how I made this graduation wreath. It's not difficult to make and works as a nice personalized decor piece for a graduation party or even as a thoughtful DIY gift.</p>


        	<a href="step12.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step12.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>
        	
        	<br/>

        	<div class="center">
        		<p class="underline">SUPPLIES USED</p>
        	</div>	

        	 <div class="center">
        		
                <p>Wire Wreath
                <br/>Spray Paint
                <br/>Black Foam Board
                <br/>DMC Floss
				<br/>Letter Stickers / Sticker Embellishments
                <br/>Black Cardstock
                <br/>Adhesive Dots
                <br/>Photos
                <br/>String / Cord
                <br/>X-Acto Knife / Scissors
                <br/>Hot Glue Gun</p>
            </div>

            <br/>

            <p>The first thing was to decide which photos I was going to use for the wreath. I decided to keep the photos consistent by using school yearbook photos (and professionally shot baby/toddler photos). Keep in mind that (due to copyright laws) if you're going to print and purchase copies of any professional photographs, you'll need a release from the photography studio that took the photo.</p>

            <p>I picked up a wire wreath from the craft store to use as my base. I got a pretty large sized wreath because I ended up using 15 photos total, but the size you get will depend on your own preference. My wreath was originally green, so I spray painted it gold to match with the school colors.</p>

        	<a href="step1.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step1.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Next, I measured and cut out a square shape from some black foam board. This is going to make the graduation cap that I put on the inside of the wreath. I determined the size of the square based on the inside diameter of my wreath.</p>

        	<a href="step2.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step2.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Next, I made the tassel for the cap. I used DMC floss for this. The nice thing about DMC floss is that it already comes in the shape of a tassel. So all I did to make this was tie off the loop at the top, then cut through the loops at the bottom to make the fringe of the tassel.</p>

        	<a href="step3.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step3.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>This is what we have so far.</p>

        	<a href="step4.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step4.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Now is the fun part: embellishments. I decided that I wanted to write something on the cap, so I picked up some gold letter stickers. I also found some yellowjacket stickers, which is the school mascot, so I thought those would be cool to add.</p>

        	<a href="step5.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step5.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>I then laid everything out on the cap to see how it would look. I had a few different phrases that I tried out, but in the end I decided to go with congrats and the year. I also played around with the placement until I got it looking how I liked it.</p>

        	<a href="step6.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step6.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>The next step was to add a border around each of my photos. I used acid free adhesive dots to adhere the photos to black cardstock.</p>

        	<a href="step7.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step7.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Then, I cut out each photo, just eyeballing a thin border around the photos as I went.</p>

        	<a href="step8.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step8.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Next, I laid everything out to see the placement of all the components. I started at the top of the wreath with my first photo and went chronologically around the wreath clockwise until I was back at the top. I adhered the stickers on the cap and used one of those adhesive dots to attach the tassel.</p>

        	<a href="step9.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step9.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>Attaching the photos was a little trickier than I predicted. There's probably a better way to do it, but I used hot glue. I made sure to keep my hot glue gun on the lowest temperature setting so it didn't burn through the paper, and I used a zigzag pattern around the entire wreath to attach the photos. I also used a dollop of hot glue at the top and bottom of the graduation cap to attach it to the wreath as well.</p>

        	<a href="step10.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step10.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>The last step would be to add some sort of hanging element. I tied some string to the top of my wreath from which to hang.</p>

        	<a href="step11.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step11.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

        	<br/>

        	<p>And there you have it! That completed my DIY graduation wreath.</p>

        	<a href="step13.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step13.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

            <a href="step12.jpg" data-toggle="lightbox" data-gallery="DIY_Graduation_Wreath" data-type="image"><img src="step12.jpg" alt="Graduation, DIY Graduation Wreath, DIY Graduation, Graduation Gift, Graduation Party" class="img-fluid"></a>

            <br/>

            <p>This was my first attempt at making a graduation wreath, so my final product is pretty basic. But what's great about this project is that there's so much room for adding personalized elements. I focused mainly on school colors (yellow and black) and added the school mascot (yellowjacket), but it would look even better if you included references to clubs or sports or came up with really creative ways to display school spirit (maybe adding some tulle or ribbon). I also chose to do a timeline of school photos, but you could use photos only from high school years, photos of the graduate with family members, at school events with friends, or even with influential teachers or mentors. Looking back, I wish I had made a double border on my photos using yellow and black and maybe added something extra around the outside. After going through this whole process, I can look back and see areas of improvement or ways I could enhance the final product if I make another wreath in the future. Hopefully, for you, this post gives you the basic elements and some ideas for making your own personalized wreath.</p>

            <p>I hope you enjoyed this tutorial and have fun making your own DIY graduation wreath. Congratulations, and best of luck to all the graduates!</p>

