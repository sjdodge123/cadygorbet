        	<p>In today's post, I'm going to show you how easy it was for me to DIY my own wooden Christmas sign. The ones I made are pretty simple and aren't very detailed, but I've seen signs this size range from $25-$60 in home decor stores. The total of my supplies to make 2 signs only cost me around $20!</p>

        	<a href="sign2.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="sign2.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

        	<br/>
        	<br/>

            

            <p>Let's get started. Here's everything you'll need..</p>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="supplies.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <div class="center">
                <p>Wood Board (I got <a href="http://www.michaels.com/wood-pallet-plaque-by-artminds-28inx11inx.6in/10409189.html">this</a> one from Michaels using a coupon)</p>
                <p>Stencils</p>
                <p>Acrylic Paint</p>
                <p>Sponge Brush & Paint Brush</p>
                <p>Tape</p>
            </div>

            <br/>

            <p>One of the main reasons I was able to make these signs for cheap was because the stencils I used were only $1 each from my Wal-Mart. They were advertised as window stencils for putting snowy images on your windows, but a stencil is a stencil. I found three different styles: 'Merry Christmas', 'Let it Snow', and one with assorted snowflakes. If you can't find the exact stencils that I used from Wal-Mart, I did happen to find them on Amazon. The price I saw on Amazon was about $10 though. The main reason I love this DIY is because it was very inexpensive to make, so I recommend trying to find stencils for a more reasonable price. I know that Hobby Lobby, Michaels, and Joann's often have coupons for a single item, so I always recommend using those when you can.</p>

            <br/>

            <p>First, you'll want to tape your stencil onto your wood plaque so it doesn't move while you're working. I used some masking tape to tape my 'Merry Christmas' stencil to the center of my board. Your design and placement will all depend on the size of your board and which stencils you are using. For my sign, I decided to go with 'Merry Christmas' in the center, then use my snowflake stencil to add snowflakes around the rest of the sign.</p>

            <a href="step1.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step1.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>When using stencils, it's key to use a stamping technique when applying the paint. The best applicator for this would be a flat top sponge brush. I only had a regular foam brush on hand, so I cut the pointed tip off to give it a flatter top. This worked well enough for me.</p>

            <a href="brush.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="brush.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>I used my brush and stamped 'Merry Christmas' with white acrylic craft paint. It took me two coats to achieve the opacity I wanted.</p>

            <a href="step2.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step2.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>


            <a href="step3.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step3.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>If you're using a plank board like I am, you'll see that the paint does not get into the spaces in between the planks. I decided to take a detail brush and fill in the spaces where the letters would connect.</p>

            <a href="step4.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step4.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>


            <a href="step5.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step5.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>This is what it looks like after filling in the gaps.</p>

            <a href="step6.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step6.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>Once the paint is dry, you can add your next stencils. I used my snowflake stencil and stamped snowflakes randomly throughout the rest of the sign. I use the bigger flakes as well as the little dots.</p>

            <a href="step7.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step7.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>Once the snowflakes dried, that completed my sign. Like I mentioned before, my stencils were pretty simple, so I decided to keep my signs minimal and just use one color.</p>

            <a href="step8.jpg" data-toggle="lightbox" data-gallery="DIY_Christmas_Sign" data-type="image"><img src="step8.jpg" alt="DIY, DIY Christmas, DIY Christmas Sign, DIY Wood Sign" class="img-fluid"></a>

            <br/>

            <p>I love how it turned out, so I made one more using the 'Let it Snow' stencil.</p> 

