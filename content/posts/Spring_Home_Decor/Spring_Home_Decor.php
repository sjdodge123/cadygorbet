        	<p>Happy spring everyone! Now that winter is over and warmer weather is moving in, I've been adding more greenery and plants to our home. Before I jump into this post, I want to let you know that none of my plants are real. Real plants are wonderful and add more to a space than just decoration, but I've never had real plants in a home before, and I don't mind decorating with fake ones. </p>

            <p>In this post, I'm going to show some pictures of how I've styled plants around our home to spruce up the décor for springtime and share a few of my thoughts and tips about my personal style when it comes to plants. </p>

            <br/>

            <p>The first space I want to talk about is the front porch. This is one of my favorite spaces to decorate, so I was very excited when the weather warmed up enough to put out our spring flowers. We have two large planters on either side of the front door. I keep these out all year and change the contents based on the season. They've actually been empty since I took down the mini Christmas trees that were there during the holidays, but it's finally time to put out the spring/summer flowers. I added my matching <a href="http://cadygorbet.com/content/posts/DIY_Hydrangea_Wreath/DIY_Hydrangea_Wreath.php">DIY hydrangea wreath</a> onto the front door, and it instantly feels like spring.</p>

            <a href="porchbeforeandafter1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="porchbeforeandafter1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Front Porch Inspo" class="img-fluid"></a>

            <a href="porchbeforeandafter2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="porchbeforeandafter2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Front Porch Inspo" class="img-fluid"></a>

            

            <p>For anyone interested, the flower stems in the planters are from At Home. Each individual stem is made of the blue, purple, and white flowers and leaves. I think they have a good variety of color that doesn't require arranging a bunch of different stems. When I was making the wreath, I was sure to match the colors of the hydrangeas to the stems. I love this wreath because it's full, fresh, and adds a lot without being too busy. It was also very easy to make. I have a <a href="http://cadygorbet.com/content/posts/DIY_Hydrangea_Wreath/DIY_Hydrangea_Wreath.php">tutorial</a> if you're interested in seeing how I made it.</p>

            <p>To finish off the front porch, I added some hanging flower baskets (that have honestly seen better days), and at the bottom of the steps I have a little garden gnome. I got him at Walmart in the section with all their new outdoor gardening décor and accessories. He was too cute to pass up. </p>

            <a href="porchoverview.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="porchoverview.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <a href="gnome.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="gnome.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>The next space I want to talk about is our living room. Since moving in, I knew I wanted plants around the fireplace. I love the look of plants in a home, but it's been a little difficult finding ones that work in this space. This is how the space looked before..</p>

            <a href="hearthbefore.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="hearthbefore.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <p>I had a lot of smaller succulent plants on one side, and some stacked birch wood on the other. I love the birch wood for fall/winter, but I didn't love the look for spring/summer. And the plants that I did have on the hearth were just too small and short to fill out the space. This is how it looks now..</p>

            <a href="hearthafter.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="hearthafter.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <p>Adding greenery to your décor brings more life and light into a space. I put the birch wood away, moved the two concrete succulent planters to the left side, and added a larger fern plant to the right. Since our living room has a lot of light, neutral colors, adding greenery brought in a nice pop of color. </p>

            <a href="hearth2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="hearth2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <a href="hearth1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="hearth1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Basket Planter" class="img-fluid"></a>


            <p>This fern is actually three fern stems that I tied together and stuck into a terracotta pot. I added black rocks from the Dollar Tree to hide the tie and give it a finished look. Then I stuck the entire pot into a basket from Home Goods. I've been loving the basket planter trend going around lately, and I finally got on board. I have another example coming up later in the post.</p>

            <a href="fireplaceoverview2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="fireplaceoverview2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Basket Planter, Fireplace Inspo" class="img-fluid"></a>


            <p>The hearth still looks a little unbalanced to me, but the fern definitely fills out the space more than the smaller potted succulents did. I'm always changing, rearranging, or slowly adding to things, but for now I really love how much better this looks. </p>

            <br/>

            <p>Moving up, let's talk about the mantle. I had such cute ideas for the mantle when we first moved in, but as soon as we mounted the TV and soundbar, I barely had any room left to work with. Because there is only a small width of open space on either side of the TV, I thought these tall, artificial topiary plants from Kirklands would work perfectly. </p>

            <a href="smalltopiary1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="smalltopiary1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary" class="img-fluid"></a>

            <a href="smalltopiary2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="smalltopiary2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary" class="img-fluid"></a>

            <p>I've had some mixed reactions to the topiary plants, but I like them. I wanted something with height on the ends of the mantle to balance out the length of the 'home sweet home' sign, and I think they're the perfect fit. Overall, I love the color and fresh atmosphere of the room now. It definitely has me feeling ready for warmer weather and more sunshine. </p>

            <a href="fireplaceoverview1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="fireplaceoverview1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>Speaking of topiary plants, my mom was actually one of the people who liked them, so when we were out browsing Big Lots one day, she ended up getting some larger ones for her house. I styled them on my hearth to give some different topiary inspiration for this blog, but I didn't purchase any myself since I already had the smaller ones on the mantle. I do really love how they look on the hearth though. </p>

            <a href="largetopiary1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="largetopiary1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary" class="img-fluid"></a>

            <p>As I mentioned earlier, I feel like my space is still a little uneven between my larger fern and smaller succulents, so getting two of the same plant to frame an area is an easy way to instantly get balance. </p>

            <a href="largetopiary2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="largetopiary2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary" class="img-fluid"></a>

            <p>I also styled these on the front porch. If you're not into flowers, I think the slim, structured design of topiary plants are perfect for framing a door. </p>

            <a href="largetopiary3.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="largetopiary3.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary, Hydrangea Wreath, Front Porch Inspo" class="img-fluid"></a>

            <a href="largetopiary4.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="largetopiary4.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Topiary" class="img-fluid"></a>

            <p>These particular ones have fairy lights in them, too, so they'd be really cute accents for any outdoor living space. Big Lots had them styled with their patio furniture displays, and that's what my mom originally purchased them for. She actually ended up using them separately by putting one on her back patio steps and the other in her living room.</p>

            <br/>

            <p>Another new addition for spring is this succulent centerpiece I put together for our coffee table. I love succulents, so I've collected quite a few different little pots of them. I decided to gather a bunch of them and put them into one large arrangement.</p>  

            <a href="succulents.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="succulents.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Succulents" class="img-fluid"></a>

            <a href="succulentcenterpiece.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="succulentcenterpiece.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Succulents, DIY" class="img-fluid"></a>

            <p>The wooden container is from Michaels, and I got the rocks at Walmart. A tip I have when doing arrangements like this is to use some sort of paper filler in the bottom of your container. I filled the bottom of this one with kraft paper, so I didn't have to buy as many rocks. </p> 

            <br/>

            <p>Next is our office plant. This is the other plant that I styled using the basket planter trend. Again, I got this basket at Home Goods, and I love it. The only problem I have is that I find it a tad too large for the plant. I wish it were a little smaller, but I used some brown kraft paper to fill in the gaps. </p>

            <a href="officeplant.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="officeplant.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Basket Planter" class="img-fluid"></a>

            <p>With this plant and the fern, I found that they sat too far down in the basket on their own. So, a quick fix I came up with was to prop the plant up using styrofoam. I got a couple styrofoam rounds at the Dollar Tree and stacked them in the bottom of the basket until I got the height I liked best. It worked great and cost me $2 (if you use this tip, be sure to use the white foam, not the softer green foam).</p>

            <br/>

            <p>All but one of the plants in our spare bedroom are on the smaller size, but I still think they add a nice spring/summer touch. This first one was a summer stem that I got at Michaels last year. </p>

            <a href="palmplant.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="palmplant.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Beach Decor, Palm" class="img-fluid"></a>

            <p>Since it was a a stem, I bought and painted a terracotta pot and 'planted' it using some styrofoam. I love the beachy vibes of this one, so I actually used some crushed seashells to cover the styrofoam and fill up the rest of the pot. To pair with the plant, I found this cute wooden block succulent at And That. I don't think I have ever really seen a wooden plant/planter cutout before, so I thought it was a unique 'plant' decor piece. And it was only $3. Side note: And That is a great place to get small planters and garden accessories. They have really cute tiny pots and a lot stuff for succulents/cacti that are really inexpensive. (When I picked up the wooden succulent piece, I also bought two small concrete planters for $2.99 a piece. You can follow my blog Instagram @cadygorbet to see how I will be using those).</p>

            <p>Next, I have this larger plant from At Home. I thought It matched the first plant nicely, and since it's larger it makes a much bigger impact in the space.</p>

            <a href="sparebedroomplant.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="sparebedroomplant.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Beach Decor, Palm Plant" class="img-fluid"></a>

            <br/>

            <p>Also in the spare bedroom, I found these hanging succulent planters at Big Lots. I thought having some planters on the wall was a cute and different way to incorporate greenery to a space. </p>

            <a href="wallsucculents1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="wallsucculents1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Succulents" class="img-fluid"></a>

            <p>I have a few other gold accents in this room, so these match perfectly. Also the white/gold combo and geometric shape make these very Pinterest-y. </p>

            <a href="wallsucculents2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="wallsucculents2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Succulents" class="img-fluid"></a>

            <br/>
            <br/>

            <p>One of my favorite new pieces is the circle shelf I got from Big Lots. I'm using it to display the cutest little cactus tealights (Amazon.com).</p>

            <a href="circleshelf1.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="circleshelf1.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Circle Shelf, Rustic Decor, Cacti" class="img-fluid"></a>

            <p>I think between the cacti, the weathered wood, and circular shape, it's giving off major modern desert vibes. This is also in our spare bedroom, so the pops of green from all of these plant décor pieces help to round out the greenery within the space.</p>

            <a href="circleshelf2.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="circleshelf2.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Circle Shelf, Rustic Decor, Cacti" class="img-fluid"></a>

            <br/>
            <br/>

            <p>Here's an overview of a few of the bigger statement pieces in the room. </p>

            <br/>

            <a href="sparebedroomoverview.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="sparebedroomoverview.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Beach Decor" class="img-fluid"></a>

            <p>The hanging planter on the right is from Marshalls. I wasn't so sure I 100% liked it, but I thought it was a unique way to display plants. I also love how all three of these pieces incorporate wood. Together, they almost give off a beachy/driftwood vibe that pairs nicely with the the blue comfortor.</p>

            <br/>

            <p>The last plant I have to share is actually the first one I got for our home. It's a small monstera plant that I purchased from Michaels last summer. I keep it on the counter in the guest bath, and it has really livened up the confined space.</p>

            <a href="bathroomplant.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="bathroomplant.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor" class="img-fluid"></a>

            <br/>
            <br/>

            <p>This has been the bulk of the new plants and greenery I've been adding around the house. As I mentioned previously, I am a big fan of little cacti and succulents, so I also have those sporadically placed on some shelves and bookcases throughout the house. But I think the larger pieces definitely revitalized our space just in time for spring. </p>

            <p>I want to mention a few more things about my plants before I end this post. I think it's important to say that though I don't have to actively take care of any living plants, fake plants do need up keeping, too. Fake plants can get very dusty, so it's good to periodically clean them off to keep them looking nice. When I'm browsing for greenery, I look for plants with a richer or deeper color. I don't typically care for plants that are too bright or neon looking or have a lot of white in the leaves, since usually those don't look as nice or realistic to me. I also like my plants to be a bit more structured, so plants that cascade or overtake a whole area (like ivy) aren't my favorites. I tend to gravitate towards palms, succulents, and plants that aren't too leafy. These are just my preferences, but plants of any kind are always a great addition to anyone's home. </p>

            <p>Thank you for reading. As always, I hope you found some useful tips and/or inspiration from this post. And, if you don't have any, I would definitely recommend picking up some plants/flowers, real or fake, to incorporate around your home. They'll be a great addition just in time for spring! </p>

            <br/>
            <br/>

            <p>UPDATE: Hey everyone! When I was talking about the new plants on the hearth in the beginning of the post, I had mentioned that the greenery was unbalanced. Well, I was at Kirklands the other day and picked up this magnolia wreath.</p>

            <a href="wreath.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="wreath.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Magnolia Wreath" class="img-fluid"></a>


            <p>I decided to put it in the nook to the left of the fireplace to even out the plants.</p>

            <a href="wreathoverview.jpg" data-toggle="lightbox" data-gallery="Spring_Home_Decor" data-type="image"><img src="wreathoverview.jpg" alt="Spring Home Decor, Home Decor, Plants, Plant Decor, Livingroom Inspo, Magnolia Wreath, Succulents, Topiary" class="img-fluid"></a>

            <p>The space looks so much more balanced to me now. The plan in that nook is to eventually DIY some shelves for a built in look, so I just never had anything up on the wall over there. But until we get around to doing the shelves, the wreath works great.</p>



