            <p>I had been looking for a small lemon tree to put on the sofa table, but I never found one that worked. Kirklands had a really nice (pricey) one, but it was too large for the space. I ended up finding some lemon branch stems at Joann Fabrics. I just put them in a glass bottle, and it gave the effect that I was going for.</p>

            <a href="sofatable1.jpg" data-toggle="lightbox" data-gallery="Summer_Lemon_Decor" data-type="image"><img src="sofatable1.jpg" alt="Summer Home Decor, Lemon Decor, Lemon Home Decor, Summery Decor, Lemons" class="img-fluid"></a>

