            <p>I typically like to change out a few decor pieces in my home to reflect the current season or holiday. This summer, I've really been liking lemons and pops of yellow. I picked up a few new items recently that I styled in our main living spaces. The lemon theme turned out really cute and makes the house feel bright and summery. Here's how it all looks..</p>

