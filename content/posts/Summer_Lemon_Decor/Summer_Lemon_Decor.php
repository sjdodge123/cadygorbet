            <p>On the buffet table, rather than adding another bottle of lemon branches, I opted for these yellow and white stems from Hobby Lobby. They compliment the theme without going overboard on the lemons.</p>

            <a href="lemons.jpg" data-toggle="lightbox" data-gallery="Summer_Lemon_Decor" data-type="image"><img src="lemons.jpg" alt="Summer Home Decor, Lemon Decor, Lemon Home Decor, Summery Decor, Lemons" class="img-fluid"></a>
           
            <p>Of course if you're going to have lemon themed decor, you're going to need a bowl of (faux) lemons. I had seen some really nice ones at Kirklands that had leaves on them, and I thought they looked nicer with a pop of green rather than just plain lemons. Unfortunately, they sold out, but I DIYed the look myself. I bought a bag of plain lemons from Michaels then pulled leaves off of some stems I had left over from a wreath I made. I was going to glue the leaves onto the lemons, but I actually ended up just placing the leaves in the bowl to make it look like they were attached to the fruit.</p>

            <br/>

            <a href="lemoncenterpiece1.jpg" data-toggle="lightbox" data-gallery="Summer_Lemon_Decor" data-type="image"><img src="lemoncenterpiece1.jpg" alt="Summer Home Decor, Lemon Decor, Lemon Home Decor, Summery Decor, Lemons" class="img-fluid"></a>
            
            <p>This circle garland/tiny wreath with lemons is from At Home. It's actually the piece I first saw and purchased that inspired the lemon theme. I was going to hang it on the wall as a tiny accent wreath, but I ended up using it for the dining table centerpiece. </p>

            <a href="lemoncenterpiece2.jpg" data-toggle="lightbox" data-gallery="Summer_Lemon_Decor" data-type="image"><img src="lemoncenterpiece2.jpg" alt="Summer Home Decor, Lemon Decor, Lemon Home Decor, Summery Decor, Lemons" class="img-fluid"></a>

        
