            <p>These metallic pumpkins are from the Dollar Tree. I was so excited when I saw them, because they look really nice and high quality, but they were $1 each! I was actually really surprised by the quality of the fall pumpkins this year at Dollar Tree. I made a post on my <a href="https://www.instagram.com/cadygorbet/">blog Instagram</a> back at the beginning of August, when Dollar Tree was putting out there fall decor, talking about how amazing their pumpkins were this year. I actually went back the next day to get a pumpkin for my cousins and aunts, and the shelf was almost cleared out. The Dollar Tree is always getting different stuff, and seasonal items can sell out quick, so I recommend checking them out once every week or two to see what they have. I also know that not all stores carry the same items, so sometimes you just have to get lucky.</p>
     
