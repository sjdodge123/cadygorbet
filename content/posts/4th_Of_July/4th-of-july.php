                <p>This flag print letter is a DIY, and I couldn’t be happier with how 
                    it turned out. I came across a picture on Pinterest that linked to 
                    an Etsy store that sells personalized letters, and decided to make 
                    my own. I purchased a plain white letter at Michaels, and gave the 
                    entire letter a base of satin white acrylic paint. I did this to 
                    mask any scuffs or scratches the letter already had, and also so 
                    the white stripes would have a satin finish like the rest of the 
                    painted areas. If I had left the white stripes as the original 
                    white of the letter, it would have had a more matte finish. I used 
                    painters tape to tape off the stripes and used clear contact paper 
                    to make my own star stickers to use as stencils. Very easy. Very 
                    cute. I made one for my mom as well. This is hang on the house 
                    outside our front door. </p>

