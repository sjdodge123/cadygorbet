            <p>This project transpired when I decided we needed something to go over the bed in our guest room to fill up the empty wall space. I thought a canvas art piece would look nice if I could find one to match the color palette of the guest room. But canvas art can be pretty pricey, especially if you're in the market for something fairly large. And it can be difficult to find something that really fits the space perfectly. I had a specific vision in mind, and I didn't want to spend a lot of money on something I wasn't 100% sure of. I've seen a lot DIY abstract art pieces on Pinterest, so I decided I would make my own. Here's how I did it..</p>


            <div class="center">
        		<p class="underline">SUPPLY LIST</p>
        	</div>	

        	<div class="center">
                <p>Canvas – 24x48”
                <br/>Acrylic Paint
                <br/>Gold Leaf
                <br/>Gold Leaf Adhesive
				<br/>Paint Brushes
                <br/>Foam Brushes 
                <br/>You might also want some sort of drop cloth to protect your workspace</p>
            </div>

            <a href="supplies.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="supplies.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

            <br/>

			<p>First, determine the canvas size. I went with a 24x48” so it would be about the width of the bed. I wasn't in a rush with this project, so I waited to purchase my canvas from Michaels when they went on a good sale. I ended up getting my canvas for 70% off! I also went with the level 1 canvas, which is the thinnest and least expensive canvas at Michaels. It worked great for this project, and I wasn't about to pay more for a 'better' canvas when I had never even painted on one before.</p>
			
			<p>Next, pick a color palette. I knew I wanted to use navy and gold on my canvas. I picked up 3 Americana Premium Acrylic Paints from Michaels. I went with the color Phthalo Turquoise, which is very much a turquoise color when using only one coat, but with 2-3 coats looks navy. And instead of using gold paint, I chose to use gold leafing. I thought it would give a nice texture and metallic shine to the piece, so I picked up a 25 sheet pack at Michaels in gold. Oddly, they didn't seem to sell any gold leaf adhesive, so I picked up a bottle from Hobby Lobby. Hobby Lobby also sold a gold leaf sealer, which would probably be a good thing to get, but I didn't purchase it.</p>
			
			<p>The last thing to get is some brushes. I got some bristle brushes in various sizes from Wal-Mart and a pack of assorted foam brushes from the Dollar Tree.</p>

			<br/>

			<p>To begin, I laid out some kraft paper to protect my workspace. I set my canvas down on top, and got some paint out onto a paper plate (my makeshift pallet).</p>

			<a href="paintingsupplies1.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="paintingsupplies1.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>When thinking about the design, I went back and forth with ideas. Did I want to use different shades of blue? Should I leave some white space? Should I try color blocking?... I ended up keeping the design simple and the technique easy by painting the whole canvas navy. I used the largest of the bristle brushes I had for this. It took at least 2 coats of paint to achieve the dark navy color I was looking for. The brush I used also shed and left some hairs in the paint, but I just left them.</p>

			<a href="halfpainted.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="halfpainted.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I applied the paint to the canvas using vertical brush strokes. The brush strokes left textured lines in the paint, so I was careful to keep them consistent as part of my design.</p>

			<a href="paintedcanvas.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="paintedcanvas.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I also painted the sides of my canvas.</p>

			<a href="paintedsides.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="paintedsides.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>Once the paint dried, I moved on to the gold leaf.</p>

			<a href="goldleafsupplies1.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="goldleafsupplies1.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I used a foam brush to apply the glue to the canvas in small sections. The directions on my glue said to wait 30 minutes for the glue to become tacky before adhearing the gold leaf, but just be sure to read the specific instructions for your supplies if you recreate this project. I kept with the vertical design as a applied the glue, sort of in a random, patchy pattern. My goal was to make the gold have the 'dry brush' painting technique look on top of the navy.</p>

			<a href="goldleafglue.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="goldleafglue.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>Once tacky, it's time to add the gold leaf. This was my first time using gold leaf, and I discovered it can be a litte tricky to use. I would recommend a bit of practice before going straight on the canvas with the gold leaf, but I just learned as I went. With my first sheet, I laid the whole sheet over one of the tacky areas. My instructions then said to brush away the excess leaf with a clean brush.</p>

			<a href="goldleafing1.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="goldleafing1.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I felt like that was wasting a lot of the gold leaf, so after the first sheet, I just took smaller pieces of the leaf and placed them more deliberately. *Gold leaf is extremely delicate, so be careful when handling it.* I used one of my smaller bristled brushes to brush away the excess.</p>

			<a href="goldleafing2.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="goldleafing2.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I experimented with different application techniques as I went. I even tried placing small pieces of the leaf onto a foam brush and brushing them onto the glue.</p>

			<a href="goldleafing4.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="goldleafing4.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>Halfway done...</p>

			<a href="halfleafed.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="halfleafed.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>I also did some leafing on the sides of the canvas.</p>

			<a href="sideleafed.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="sideleafed.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>This is what the completed canvas looked like. I'm very happy with the results.</p>

			<a href="finishedcanvas2.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="finishedcanvas2.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>Here's a close up of how the gold leafing looks.</p>

			<a href="closeup1.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="closeup1.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>
			
			<p>And that's the completed canvas. It was easy, even considering this was my first time working on a canvas and with gold leaf, and it didn't take very long to do. The most time you'll put into it is waiting for the paint and glue to dry. It was also a lot cheaper to make than any canvas that size would have been to purchase in a store. And I like knowing that if anyone ever asks where I got my canvas, I can say that I made it myself!</p>

			<p>Here's the canvas looking great hung above the guest bed. And because I got to paint my own design, it matches perfectly with the bed's accent pillows.</p>

			<a href="finishedcanvas4.jpg" data-toggle="lightbox" data-gallery="Easy_DIY_Canvas_Art" data-type="image"><img src="finishedcanvas4.jpg" alt="Easy DIY Canvas Art, DIY Home Decor, Canvas Art" class="img-fluid"></a>

			<br/>

			<p>Thanks so much for reading. I hope you enjoyed this project, learned something, and got inspired to DIY!</p>

